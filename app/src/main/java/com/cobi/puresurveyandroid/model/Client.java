package com.cobi.puresurveyandroid.model;

public class Client {
    public String customerName;
    public String tel;
    public String cell;
    public String email;

    public Client(String customerName, String tel, String cell, String email) {
        this.customerName = customerName;
        this.tel = tel;
        this.cell = cell;
        this.email = email;
    }

}
